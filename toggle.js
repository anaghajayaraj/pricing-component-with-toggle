function togglePrice() {
    var x = document.getElementsByClassName('monthly-price')[0];
    var y = document.getElementsByClassName('annual-price')[0];
    if (x.style.display === "none") {
      x.style.display = "block";
      y.style.display="none";
    }
    else {
      x.style.display = "none";
      y.style.display="block";
    }
    var x = document.getElementsByClassName('monthly-price')[1];
    var y = document.getElementsByClassName('annual-price')[1];
    if (x.style.display === "none") {
      x.style.display = "block";
      y.style.display="none";
    }
    else {
      x.style.display = "none";
      y.style.display="block";
    }
    var x = document.getElementsByClassName('monthly-price')[2];
    var y = document.getElementsByClassName('annual-price')[2];
    if (x.style.display === "none") {
      x.style.display = "block";
      y.style.display="none";
    }
    else {
      x.style.display = "none";
      y.style.display="block";
    }
    
  }